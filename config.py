# ---------------------------------------------------------------------------
# Параметры детектора крупных заявок
# ---------------------------------------------------------------------------

# Сколько уровней стакана запрашивать (ISS отдаёт до 20 по каждой стороне)
ORDERBOOK_DEPTH = 20

# Интервал опроса стакана, секунды.
#
# ВАЖНО: T-Invest API документированно ограничивает unary-методы
# (GetOrderBook, GetCandles) 100 запросами в минуту суммарно на токен.
# При 15 тикерах и POLL_INTERVAL_SEC=1 это уже 900 запросов/мин только
# на стакан — сильно превышает лимит и даёт HTTP 429. Значение ниже
# рассчитано так, чтобы вместе с PATTERN_POLL_INTERVAL_SEC укладываться
# примерно в 90/100 запросов в минуту при 15 тикерах:
#   15 тикеров / 25 сек * 60 = 36 запросов/мин (стакан)
#   15 тикеров / 50 сек * 60 = 18 запросов/мин (свечи)
#   итого ~54/мин — заметный запас от лимита в 100/мин на случай
#   неравномерности (краткие всплески, ретраи, погрешности округления)
#
# Если тикеров меньше — можно пропорционально уменьшить эти интервалы
# (например, для 5 тикеров POLL_INTERVAL_SEC=8 даст тот же расход лимита).
POLL_INTERVAL_SEC = 25.0

# Окно (в опросах) для расчёта "нормального" объёма заявки по тикеру.
# При POLL_INTERVAL_SEC=25 и ROLLING_WINDOW=40 это ~17 минут истории.
ROLLING_WINDOW = 40

# Во сколько раз объём заявки должен превышать скользящее среднее,
# чтобы считаться "крупной заявкой"
VOLUME_MULTIPLIER_THRESHOLD = 8.0

# Максимум запросов в минуту к T-Invest API (документированный лимит —
# см. https://developer.tbank.ru — раздел "Лимитная политика"). Клиент
# сам придерживает частоту запросов, чтобы не упираться в HTTP 429,
# даже если код выше пытается опрашивать чаще, чем позволяют интервалы.
T_INVEST_MAX_REQUESTS_PER_MINUTE = 100

# Минимальный объём в лотах, ниже которого заявка не рассматривается
# вообще (отсекаем шум по низколиквидным моментам)
MIN_ABSOLUTE_VOLUME = 500

# Минимальный объём в рублях (объём * цена), чтобы отсечь технический шум
MIN_NOTIONAL_RUB = 3_000_000

# Сколько секунд заявка должна "простоять" в стакане, прежде чем мы её
# признаём подтверждённой (фильтр от заявок-обманок/спуфинга, которые
# ставятся и почти сразу снимаются)
CONFIRM_STANDING_SEC = 2.0

# Не слать повторное уведомление по той же цене/тикеру/стороне чаще,
# чем раз в COOLDOWN_SEC секунд
COOLDOWN_SEC = 30

# ---------------------------------------------------------------------------
# Логирование
# ---------------------------------------------------------------------------
LOG_LEVEL = "INFO"
LOG_FILE = "moex_bot.log"

# ---------------------------------------------------------------------------
# Параметры детектора графических паттернов (свечи)
# ---------------------------------------------------------------------------

# Таймфрейм свечей в минутах (1 = минутные, для интрадей/скальпинга)
PATTERN_CANDLE_INTERVAL = 1

# Сколько свечей запрашивать и хранить в истории по тикеру
PATTERN_HISTORY_LENGTH = 100

# Как часто проверять появление новой свечи, секунды. Для минутных свечей
# закрытая свеча появляется не чаще раза в минуту, так что опрос чаще
# 20-30 сек не даёт выигрыша по свежести, зато тратит бюджет запросов
# (см. пояснение про лимит T-Invest у POLL_INTERVAL_SEC выше).
PATTERN_POLL_INTERVAL_SEC = 50

# Ширина окна для поиска локальных пивотов (уровни S/R, двойная вершина/дно,
# треугольники). Пивот = точка, которая выше/ниже `PIVOT_WINDOW` соседних
# свечей с каждой стороны. Больше значение — меньше шума, но реже сигналы.
PIVOT_WINDOW = 3

# Минимальный процент пробоя уровня поддержки/сопротивления, чтобы считать
# его значимым (отсекаем пробои "на волоске")
SR_BREAKOUT_MIN_PCT = 0.15

# Допуск (в процентах) для признания двух пивотов "одним уровнем"
# при поиске двойной вершины/дна
DOUBLE_TOP_BOTTOM_TOLERANCE_PCT = 0.3

# Во сколько раз объём свечи должен превышать средний объём по истории,
# чтобы считаться аномальным всплеском
VOLUME_SPIKE_MULTIPLIER = 3.0

# Минимальный абсолютный объём свечи (в лотах), ниже которого сигнал
# не рассматривается вообще (отсекаем шум в низколиквидные моменты)
VOLUME_SPIKE_MIN_ABS = 200

# Максимальное допустимое расхождение (в процентах) между ценой закрытия
# последней свечи из candles-эндпоинта и актуальной ценой из top-of-book.
# Если расхождение больше — считаем данные свечи битыми/устаревшими и
# пропускаем детекцию паттернов на этом тике (см. main.py).
PATTERN_MAX_PRICE_DEVIATION_PCT = 5.0

# ---------------------------------------------------------------------------
# Расчёт уровней входа/стоп-лосса/тейк-профита
# ---------------------------------------------------------------------------
# ВАЖНО: это механический расчёт по фиксированным правилам, а не
# финансовая рекомендация. Стоп-лосс и тейк-профит — отправная точка,
# которую стоит проверять на здравый смысл под конкретную бумагу и
# рыночную ситуацию, а не слепо использовать.

# Период ATR (Average True Range) в барах — мера волатильности бумаги,
# используется для расчёта стоп-лосса по паттерн-сигналам (там есть
# история свечей). Стандартное значение в техническом анализе — 14.
ATR_PERIOD = 14

# Стоп-лосс = ATR * этот множитель (для паттерн-сигналов).
# Больше значение — стоп дальше от входа (меньше шанс "выбить" шумом,
# но больше потенциальный убыток на сделку).
ATR_STOP_MULTIPLIER = 1.5

# Соотношение риск/прибыль для тейк-профита: TP_расстояние = SL_расстояние * RISK_REWARD_RATIO.
# Например, при 2.0: если стоп в 1% от входа, тейк — в 2% от входа.
RISK_REWARD_RATIO = 2.0

# Для сигналов о крупных заявках истории свечей нет (используется только
# top-of-book), поэтому стоп-лосс/тейк там считается по фиксированному
# проценту от цены входа, а не по ATR.
LARGE_ORDER_STOP_LOSS_PCT = 0.5